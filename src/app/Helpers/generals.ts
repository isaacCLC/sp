  timeBetweenTwoDates(endDate: any) {
    const currDate = new Date();
    var diff = currDate.getTime() - endDate.getTime();
    let mins = Math.round(diff / 60000);
    if (mins < 60) {
      if (Math.round(mins) == 1) {
        return Math.round(mins) + " minute  ago";
      } else return Math.round(mins) + " minutes ago";
    } else if (mins > 59 && mins < 1440) {
      if (Math.round(mins / 60) == 1) {
        return Math.round(mins / 60) + " hr ago";
      } else return Math.round(mins / 60) + " hrs ago";
    } else if (mins > 1439 && mins < 10080) {
      if (Math.round(mins / 1440) == 1) {
        return Math.round(mins / 1440) + " day ago";
      } else return Math.round(mins / 1440) + " days ago";
    } else if (mins > 10079) {
      if (Math.round(mins / 10080) == 1) {
        return Math.round(mins / 10080) + " week ago";
      } else return Math.round(mins / 10080) + " weeks ago";
    }
  }

  validateCell(cellNum: string): boolean {
    let isnum = /^\d+$/.test(cellNum);
    if (cellNum != undefined) {
      if (cellNum.length > 9 && isnum == true && cellNum.substr(0, 1) == "0") {
        return true;
      } else {
        return false;
      }
    }
    return true;
  }

